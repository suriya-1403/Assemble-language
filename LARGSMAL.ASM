DATA SEGMENT
NUM DW 04H,05H,03H,01H,02H
LARG DW ?
SMAL DW ?
COUNT EQU 05H
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
    MOV AX,DATA
    MOV DS,AX
    MOV CX,COUNT
    MOV AX,0FFFFH
    MOV BX,0000H
LPT:
    CMP AX,NUM[SI]
    JC END1
    MOV AX,NUM[SI]
END1:
    CMP NUM[SI],BX
    JC END2
    MOV BX,NUM[SI]
END2:
    INC SI
    INC SI
    LOOP LPT
    MOV SMAL,AX
    MOV LARG,BX
HLT
CODE ENDS
END START