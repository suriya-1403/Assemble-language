DATA SEGMENT
    ROCOL EQU 03H
    MAT1 DB 01H,01H,01H,02H,02H,02H,03H,03H,03H
    MAT2 DB 04H,04H,04H,05H,05H,05H,06H,06H,06H
    PMAT DB 09 DUP(?)
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
    MOV AX,DATA
    MOV DS,AX
    MOV CH,ROCOL
    MOV BX,OFFSET PMAT 
    MOV SI,OFFSET MAT1
NR:
    MOV DI,OFFSET MAT2
    MOV CL,ROCOL
NC:
    MOV DL,ROCOL
    MOV BP,00H
    MOV AX,00H
    SAHF
R1:  
    MOV AL,[SI]
    MUL BYTE PTR[DI]
    ADD BP,AX
    INC SI
    ADD DI,03H
    DEC DL
    JNZ R1
    SUB DI,08
    SUB SI,03
    MOV [BX],BP
    ADD BX,01
    DEC CL
    JNZ NC
    ADD SI,03
    DEC CH
    JNZ NR
HLT
CODE ENDS
END START